private val darkColorScheme = darkColorScheme(
    primary = Purple200,
    inversePrimary = Purple700,
    secondary = Teal200,
    outline = outline

)

private val lightColorScheme = lightColorScheme(
    primary = Purple500,
    inversePrimary = Purple700,
    secondary = Teal200,
    outline = outline

    /* Other default colors to override
    background = Color.White,
    surface = Color.White,
